const MOCK_OPERATOR_ID = 'mock-system-admin'
const MOCK_OPERATOR_NAME = 'Mock System Admin'
const MOCK_TIMESTAMP = new Date('2026-04-03T09:00:00+08:00')

type MenuSeedInput = Partial<SysMenu> & Pick<SysMenu, 'id' | 'title' | 'menuType'>

function createMenuSeed(input: MenuSeedInput): SysMenu {
  return {
    id: input.id,
    createId: input.createId ?? MOCK_OPERATOR_ID,
    createName: input.createName ?? MOCK_OPERATOR_NAME,
    createTime: input.createTime ?? new Date(MOCK_TIMESTAMP),
    updateId: input.updateId ?? MOCK_OPERATOR_ID,
    updateName: input.updateName ?? MOCK_OPERATOR_NAME,
    updateTime: input.updateTime ?? new Date(MOCK_TIMESTAMP),
    code: input.code ?? null,
    isDelete: input.isDelete ?? false,
    parentId: input.parentId ?? null,
    parentPath: input.parentPath ?? null,
    sort: input.sort ?? 0,
    title: input.title,
    permission: input.permission ?? null,
    menuType: input.menuType,
    path: input.path ?? null,
    icon: input.icon ?? null,
    component: input.component ?? null,
    locale: input.locale ?? null,
    menuStatus: input.menuStatus ?? 0,
    affix: input.affix ?? 0,
    redirect: input.redirect ?? null,
    name: input.name ?? null,
    hideInMenu: input.hideInMenu ?? 0,
    parentKeys: input.parentKeys ?? null,
    url: input.url ?? null,
    hideInBreadcrumb: input.hideInBreadcrumb ?? 0,
    hideChildrenInMenu: input.hideChildrenInMenu ?? 0,
    keepAlive: input.keepAlive ?? 0,
    target: input.target ?? null,
  }
}

